'''
Tipos de dados
str - string
int - inteiro - 123456 0 - 10 - 20 - 50 - 100
float - real/ponto flutuante - 10.50 - 1.5 - 1.100
bool - boleano/lógico - True/False 10 == 10
'''

# STRING: NOME
print('Nícolas Mota', type('Nícolas Mota'))

# IDADE: INT
print(18, type(18))

# ALTURA: FLOAT
print(1.88, type(1.88))

# É MAIOR DE IDADE 10 > 20
print(21 > 18, type(21 > 18))